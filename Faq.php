                <h3 style="color:#FFd523;">Cara belinya gimana?</h3>
                <p>
                Buat tiket nonton, kamu tinggal cari film yang ingin kamu tonton, pilih tempat dan tanggal nonton, pilih kursi yang kamu inginkan, lalu lakukan pembayaran.
                <br>
                Setelah selesai melakukan pembayaran, kamu akan mendapatkan notifikasi berisi kode booking melalui e-mail dan WhatsApp kamu yang nantinya kamu tinggal tukarkan di bioskop menjadi tiket fisik.
                <br>
                Untuk tiket acara, kamu tinggal cari acara yang kamu ingin datengin mulai dari yang gratis sampai berbayar, pilih jumlah dan jenis tiket, lalu lakukan pembayaran/pemesanan. Setelah itu, kamu akan dapat notifikasi kode booking dari e-mail dan WhatsApp yang nantinya tinggal kamu tunjukin di lokasi acara untuk bisa memasuki acara itu.
            </p>      
